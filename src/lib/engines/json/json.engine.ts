function formatMarkdownCell(value: unknown): string {
	if (value === null || value === undefined) {
		return '';
	}

	if (typeof value === 'object') {
		return JSON.stringify(value).replaceAll('|', '\\|');
	}

	return String(value).replaceAll('|', '\\|').replaceAll('\n', '<br>');
}

function flattenIntoEntries(
	value: unknown,
	segments: JsonFlatPathSegment[],
	options: ResolvedJsonFlattenOptions,
	output: Map<string, unknown>
): void {
	if (isFlattenLeaf(value)) {
		output.set(buildFlatPath(segments, options), value);
		return;
	}

	if (Array.isArray(value)) {
		if (value.length === 0) {
			output.set(buildFlatPath(segments, options), []);
			return;
		}

		for (const [index, entry] of value.entries()) {
			flattenIntoEntries(entry, [...segments, index], options, output);
		}
		return;
	}

	if (isRecordObject(value)) {
		const entries = Object.entries(value);
		if (entries.length === 0) {
			output.set(buildFlatPath(segments, options), {});
			return;
		}

		for (const [key, entry] of entries) {
			flattenIntoEntries(entry, [...segments, key], options, output);
		}
		return;
	}

	output.set(buildFlatPath(segments, options), value);
}

function resolveJsonFlattenOptions(
	options: Partial<JsonFlattenOptions>,
	fallbackMode: JsonFlattenMode
): ResolvedJsonFlattenOptions {
	const mode = options.mode ?? fallbackMode;
	const separator = options.separator ?? DEFAULT_JSON_FLATTEN_OPTIONS.separator;
	if (separator.length === 0) {
		throw new Error('ui.json_flatten.error.empty_separator');
	}

	const rootKey = options.rootKey?.trim() || DEFAULT_JSON_FLATTEN_OPTIONS.rootKey;

	return {
		mode,
		separator,
		rootKey
	};
}

function buildFlatPath(
	segments: JsonFlatPathSegment[],
	options: ResolvedJsonFlattenOptions
): string {
	if (segments.length === 0) {
		return options.rootKey;
	}

	let path = '';
	for (const segment of segments) {
		if (typeof segment === 'number') {
			path += `[${segment}]`;
			continue;
		}

		const escaped = escapePathKey(segment, options.separator);
		path = path.length === 0 ? escaped : `${path}${options.separator}${escaped}`;
	}

	return path.length > 0 ? path : options.rootKey;
}

function escapePathKey(value: string, separator: string): string {
	let escaped = value.replaceAll('\\', '\\\\').replaceAll('[', '\\[').replaceAll(']', '\\]');

	if (separator.length > 0) {
		escaped = escaped.split(separator).join(`\\${separator}`);
	}

	return escaped;
}

function parseFlatPath(path: string, options: ResolvedJsonFlattenOptions): JsonFlatPathSegment[] {
	if (path === options.rootKey) {
		return [];
	}

	if (path.length === 0) {
		throw new Error('ui.json_flatten.error.invalid_path');
	}

	const rawSegments = splitPathSegments(path, options.separator);
	const tokens: JsonFlatPathSegment[] = [];

	for (const segment of rawSegments) {
		if (segment.length === 0) {
			throw new Error('ui.json_flatten.error.invalid_path');
		}
		tokens.push(...parseFlatPathSegment(segment));
	}

	if (tokens.length === 0) {
		throw new Error('ui.json_flatten.error.invalid_path');
	}

	return tokens;
}

function splitPathSegments(path: string, separator: string): string[] {
	const segments: string[] = [];
	let current = '';
	let cursor = 0;

	while (cursor < path.length) {
		const char = path[cursor];
		if (char === '\\') {
			if (cursor + 1 >= path.length) {
				throw new Error('ui.json_flatten.error.invalid_escape');
			}
			current += path.slice(cursor, cursor + 2);
			cursor += 2;
			continue;
		}

		if (path.startsWith(separator, cursor)) {
			segments.push(current);
			current = '';
			cursor += separator.length;
			continue;
		}

		current += char;
		cursor += 1;
	}

	segments.push(current);
	return segments;
}

function parseFlatPathSegment(segment: string): JsonFlatPathSegment[] {
	const tokens: JsonFlatPathSegment[] = [];
	let keyBuffer = '';
	let cursor = 0;

	while (cursor < segment.length) {
		const char = segment[cursor];

		if (char === '\\') {
			if (cursor + 1 >= segment.length) {
				throw new Error('ui.json_flatten.error.invalid_escape');
			}
			keyBuffer += segment[cursor + 1];
			cursor += 2;
			continue;
		}

		if (char === '[') {
			if (keyBuffer.length > 0) {
				tokens.push(keyBuffer);
				keyBuffer = '';
			}

			const closeIndex = segment.indexOf(']', cursor + 1);
			if (closeIndex < 0) {
				throw new Error('ui.json_flatten.error.invalid_path');
			}

			const indexToken = segment.slice(cursor + 1, closeIndex);
			if (!/^\d+$/.test(indexToken)) {
				throw new Error('ui.json_flatten.error.invalid_array_index');
			}

			if (closeIndex + 1 < segment.length && segment[closeIndex + 1] !== '[') {
				throw new Error('ui.json_flatten.error.invalid_path');
			}

			tokens.push(Number(indexToken));
			cursor = closeIndex + 1;
			continue;
		}

		if (char === ']') {
			throw new Error('ui.json_flatten.error.invalid_path');
		}

		keyBuffer += char;
		cursor += 1;
	}

	if (keyBuffer.length > 0) {
		tokens.push(keyBuffer);
	}

	return tokens;
}

function assignFlatValue(
	rootContainer: unknown,
	segments: JsonFlatPathSegment[],
	value: unknown
): void {
	let current: unknown = rootContainer;

	for (let index = 0; index < segments.length; index += 1) {
		const segment = segments[index];
		const isLast = index === segments.length - 1;
		const nextSegment = segments[index + 1];

		if (typeof segment === 'number') {
			if (!Array.isArray(current)) {
				throw new Error('ui.json_flatten.error.path_conflict');
			}

			ensureArrayIndex(current, segment);

			if (isLast) {
				if (segment in current) {
					throw new Error('ui.json_flatten.error.path_conflict');
				}
				current[segment] = value;
				return;
			}

			if (!(segment in current)) {
				current[segment] = typeof nextSegment === 'number' ? [] : {};
			} else if (!isTraversableContainer(current[segment])) {
				throw new Error('ui.json_flatten.error.path_conflict');
			}

			current = current[segment];
			continue;
		}

		assertSafeObjectKey(segment);

		if (!isRecordObject(current)) {
			throw new Error('ui.json_flatten.error.path_conflict');
		}

		if (isLast) {
			if (Object.prototype.hasOwnProperty.call(current, segment)) {
				throw new Error('ui.json_flatten.error.path_conflict');
			}
			current[segment] = value;
			return;
		}

		if (!Object.prototype.hasOwnProperty.call(current, segment)) {
			current[segment] = typeof nextSegment === 'number' ? [] : {};
		} else if (!isTraversableContainer(current[segment])) {
			throw new Error('ui.json_flatten.error.path_conflict');
		}

		current = current[segment];
	}
}

function ensureArrayIndex(target: unknown[], index: number): void {
	if (!Number.isInteger(index) || index < 0) {
		throw new Error('ui.json_flatten.error.invalid_array_index');
	}

	while (target.length < index) {
		target.push(null);
	}
}

function assertSafeObjectKey(key: string): void {
	if (PROTOTYPE_POLLUTION_KEYS.has(key)) {
		throw new Error('ui.json_flatten.error.unsafe_key');
	}
}

function isFlattenLeaf(value: unknown): boolean {
	return (
		value === null ||
		typeof value === 'string' ||
		typeof value === 'number' ||
		typeof value === 'boolean'
	);
}

function isRecordObject(value: unknown): value is Record<string, unknown> {
	return value !== null && typeof value === 'object' && !Array.isArray(value);
}

function buildJsonPatchOperations(
	base: unknown,
	target: unknown,
	path: string,
	operations: JsonPatchOperation[]
): void {
	if (isDeepEqual(base, target)) {
		return;
	}

	if (Array.isArray(base) && Array.isArray(target)) {
		buildArrayPatchOperations(base, target, path, operations);
		return;
	}

	if (isRecordObject(base) && isRecordObject(target)) {
		buildObjectPatchOperations(base, target, path, operations);
		return;
	}

	operations.push({
		op: path === '' ? 'add' : 'replace',
		path: path || '/',
		value: target
	});
}

function buildObjectPatchOperations(
	base: Record<string, unknown>,
	target: Record<string, unknown>,
	path: string,
	operations: JsonPatchOperation[]
): void {
	const baseKeys = Object.keys(base).sort();
	const targetKeys = Object.keys(target).sort();

	for (const key of baseKeys) {
		if (!Object.prototype.hasOwnProperty.call(target, key)) {
			operations.push({ op: 'remove', path: `${path}/${encodePointerToken(key)}` || '/' });
		}
	}

	for (const key of targetKeys) {
		const nextPath = `${path}/${encodePointerToken(key)}`;
		if (!Object.prototype.hasOwnProperty.call(base, key)) {
			operations.push({ op: 'add', path: nextPath, value: target[key] });
			continue;
		}

		buildJsonPatchOperations(base[key], target[key], nextPath, operations);
	}
}

function buildArrayPatchOperations(
	base: unknown[],
	target: unknown[],
	path: string,
	operations: JsonPatchOperation[]
): void {
	const sharedLength = Math.min(base.length, target.length);

	for (let index = 0; index < sharedLength; index += 1) {
		buildJsonPatchOperations(base[index], target[index], `${path}/${index}`, operations);
	}

	for (let index = base.length - 1; index >= target.length; index -= 1) {
		operations.push({ op: 'remove', path: `${path}/${index}` || '/' });
	}

	for (let index = base.length; index < target.length; index += 1) {
		operations.push({ op: 'add', path: `${path}/-` || '/-', value: target[index] });
	}
}

function normalizePatchOperation(candidate: unknown): JsonPatchOperation {
	if (!isRecordObject(candidate)) {
		throw new Error('ui.json_patch.error.invalid_operation');
	}

	const op = candidate.op;
	const path = candidate.path;
	if (typeof op !== 'string' || typeof path !== 'string') {
		throw new Error('ui.json_patch.error.invalid_operation');
	}

	if (!['add', 'remove', 'replace', 'move', 'copy', 'test'].includes(op)) {
		throw new Error('ui.json_patch.error.unsupported_operation');
	}

	if ((op === 'move' || op === 'copy') && typeof candidate.from !== 'string') {
		throw new Error('ui.json_patch.error.missing_from');
	}

	if (
		(op === 'add' || op === 'replace' || op === 'test') &&
		!Object.prototype.hasOwnProperty.call(candidate, 'value')
	) {
		throw new Error('ui.json_patch.error.missing_value');
	}

	const normalizedOp = op as JsonPatchOperation['op'];

	return {
		op: normalizedOp,
		path,
		from: typeof candidate.from === 'string' ? candidate.from : undefined,
		value: candidate.value
	};
}

function applyPatchOperation(document: unknown, operation: JsonPatchOperation): unknown {
	if (operation.path === '' || operation.path === '/') {
		if (operation.op === 'add' || operation.op === 'replace') {
			return structuredClone(operation.value);
		}
		if (operation.op === 'remove') {
			return null;
		}
		if (operation.op === 'test') {
			if (!isDeepEqual(document, operation.value)) {
				throw new Error('ui.json_patch.error.test_failed');
			}
			return document;
		}
	}

	if (operation.op === 'move' || operation.op === 'copy') {
		if (!operation.from) {
			throw new Error('ui.json_patch.error.missing_from');
		}
		const sourceValue = getByPointer(document, operation.from);
		const nextValue = operation.op === 'copy' ? structuredClone(sourceValue) : sourceValue;
		let nextDocument = document;
		if (operation.op === 'move') {
			nextDocument = removeByPointer(nextDocument, operation.from);
		}
		return addOrReplaceByPointer(nextDocument, operation.path, nextValue, 'add');
	}

	if (operation.op === 'add' || operation.op === 'replace') {
		return addOrReplaceByPointer(document, operation.path, operation.value, operation.op);
	}

	if (operation.op === 'remove') {
		return removeByPointer(document, operation.path);
	}

	if (operation.op === 'test') {
		const currentValue = getByPointer(document, operation.path);
		if (!isDeepEqual(currentValue, operation.value)) {
			throw new Error('ui.json_patch.error.test_failed');
		}
		return document;
	}

	throw new Error('ui.json_patch.error.unsupported_operation');
}

function addOrReplaceByPointer(
	document: unknown,
	pointer: string,
	value: unknown,
	mode: 'add' | 'replace'
): unknown {
	const { parent, token } = resolvePointerParent(document, pointer);

	if (Array.isArray(parent)) {
		if (token === '-') {
			if (mode === 'replace') {
				throw new Error('ui.json_patch.error.invalid_array_index');
			}
			parent.push(structuredClone(value));
			return document;
		}

		const index = Number(token);
		if (!Number.isInteger(index) || index < 0) {
			throw new Error('ui.json_patch.error.invalid_array_index');
		}

		if (mode === 'replace') {
			if (index >= parent.length) {
				throw new Error('ui.json_patch.error.path_not_found');
			}
			parent[index] = structuredClone(value);
			return document;
		}

		if (index > parent.length) {
			throw new Error('ui.json_patch.error.invalid_array_index');
		}

		parent.splice(index, 0, structuredClone(value));
		return document;
	}

	if (!isRecordObject(parent)) {
		throw new Error('ui.json_patch.error.path_not_found');
	}

	if (mode === 'replace' && !Object.prototype.hasOwnProperty.call(parent, token)) {
		throw new Error('ui.json_patch.error.path_not_found');
	}

	parent[token] = structuredClone(value);
	return document;
}

function removeByPointer(document: unknown, pointer: string): unknown {
	const { parent, token } = resolvePointerParent(document, pointer);

	if (Array.isArray(parent)) {
		const index = Number(token);
		if (!Number.isInteger(index) || index < 0 || index >= parent.length) {
			throw new Error('ui.json_patch.error.path_not_found');
		}
		parent.splice(index, 1);
		return document;
	}

	if (!isRecordObject(parent) || !Object.prototype.hasOwnProperty.call(parent, token)) {
		throw new Error('ui.json_patch.error.path_not_found');
	}

	delete parent[token];
	return document;
}

function getByPointer(document: unknown, pointer: string): unknown {
	const tokens = parsePointer(pointer);
	let current: unknown = document;

	for (const token of tokens) {
		if (Array.isArray(current)) {
			const index = Number(token);
			if (!Number.isInteger(index) || index < 0 || index >= current.length) {
				throw new Error('ui.json_patch.error.path_not_found');
			}
			current = current[index];
			continue;
		}

		if (!isRecordObject(current) || !Object.prototype.hasOwnProperty.call(current, token)) {
			throw new Error('ui.json_patch.error.path_not_found');
		}

		current = current[token];
	}

	return current;
}

function resolvePointerParent(
	document: unknown,
	pointer: string
): { parent: unknown; token: string } {
	const tokens = parsePointer(pointer);
	if (tokens.length === 0) {
		throw new Error('ui.json_patch.error.invalid_pointer');
	}

	let parent: unknown = document;
	for (let index = 0; index < tokens.length - 1; index += 1) {
		const token = tokens[index];
		if (Array.isArray(parent)) {
			const arrayIndex = Number(token);
			if (!Number.isInteger(arrayIndex) || arrayIndex < 0 || arrayIndex >= parent.length) {
				throw new Error('ui.json_patch.error.path_not_found');
			}
			parent = parent[arrayIndex];
			continue;
		}

		if (!isRecordObject(parent) || !Object.prototype.hasOwnProperty.call(parent, token)) {
			throw new Error('ui.json_patch.error.path_not_found');
		}

		parent = parent[token];
	}

	return {
		parent,
		token: tokens[tokens.length - 1] ?? ''
	};
}

function parsePointer(pointer: string): string[] {
	if (pointer === '' || pointer === '/') {
		return [];
	}

	if (!pointer.startsWith('/')) {
		throw new Error('ui.json_patch.error.invalid_pointer');
	}

	return pointer
		.slice(1)
		.split('/')
		.map((token) => token.replaceAll('~1', '/').replaceAll('~0', '~'));
}

function encodePointerToken(token: string): string {
	return token.replaceAll('~', '~0').replaceAll('/', '~1');
}

function isDeepEqual(left: unknown, right: unknown): boolean {
	if (left === right) {
		return true;
	}

	if (Array.isArray(left) && Array.isArray(right)) {
		if (left.length !== right.length) {
			return false;
		}
		for (let index = 0; index < left.length; index += 1) {
			if (!isDeepEqual(left[index], right[index])) {
				return false;
			}
		}
		return true;
	}

	if (isRecordObject(left) && isRecordObject(right)) {
		const leftKeys = Object.keys(left);
		const rightKeys = Object.keys(right);
		if (leftKeys.length !== rightKeys.length) {
			return false;
		}
		for (const key of leftKeys) {
			if (!Object.prototype.hasOwnProperty.call(right, key)) {
				return false;
			}
			if (!isDeepEqual(left[key], right[key])) {
				return false;
			}
		}
		return true;
	}

	return false;
}

function isTraversableContainer(value: unknown): boolean {
	return Array.isArray(value) || isRecordObject(value);
}

function inferSchemaFromValues(values: JsonValue[]): JsonSchema {
	if (values.length === 0) {
		return {};
	}

	if (values.every(isJsonObject)) {
		return inferObjectSchema(values);
	}

	if (values.every(isJsonArray)) {
		return inferArraySchema(values);
	}

	if (values.every(isJsonPrimitive)) {
		return inferPrimitiveSchema(values);
	}

	const variants = dedupeSchemas(values.map((value) => inferSchemaFromValues([value])));
	if (variants.length === 1) {
		return variants[0];
	}

	return { anyOf: variants };
}

function inferObjectSchema(objects: JsonObject[]): JsonSchema {
	const allKeys = new Set<string>();
	const keyCounts = new Map<string, number>();
	const valueBuckets = new Map<string, JsonValue[]>();

	for (const objectValue of objects) {
		for (const [key, value] of Object.entries(objectValue)) {
			allKeys.add(key);
			keyCounts.set(key, (keyCounts.get(key) ?? 0) + 1);
			const existingValues = valueBuckets.get(key) ?? [];
			existingValues.push(value);
			valueBuckets.set(key, existingValues);
		}
	}

	const sortedKeys = Array.from(allKeys).sort((left, right) => left.localeCompare(right));
	const properties: Record<string, JsonSchema> = {};
	for (const key of sortedKeys) {
		properties[key] = inferSchemaFromValues(valueBuckets.get(key) ?? []);
	}

	const required = sortedKeys.filter((key) => (keyCounts.get(key) ?? 0) === objects.length);

	return {
		type: 'object',
		properties,
		required: required.length > 0 ? required : undefined,
		additionalProperties: false
	};
}

function inferArraySchema(arrays: JsonArray[]): JsonSchema {
	const flattened = arrays.flat();
	return {
		type: 'array',
		items: flattened.length > 0 ? inferSchemaFromValues(flattened) : {}
	};
}

function inferPrimitiveSchema(values: JsonScalar[]): JsonSchema {
	const types = normalizePrimitiveTypes(values.map((value) => getPrimitiveSchemaType(value)));
	const enumValues = inferEnum(values);

	return {
		type: types.length === 1 ? types[0] : types,
		enum: enumValues
	};
}

function inferEnum(values: JsonScalar[]): JsonScalar[] | undefined {
	if (values.length < 2) {
		return undefined;
	}

	const uniqueValues = Array.from(new Set(values));
	if (uniqueValues.length === 0 || uniqueValues.length > 8) {
		return undefined;
	}

	if (uniqueValues.length <= 3 || uniqueValues.length < values.length) {
		return uniqueValues;
	}

	return undefined;
}

function normalizePrimitiveTypes(types: JsonSchemaType[]): JsonSchemaType[] {
	const unique = Array.from(new Set(types));
	if (unique.includes('number') && unique.includes('integer')) {
		return unique.filter((type) => type !== 'integer');
	}
	return unique;
}

function getPrimitiveSchemaType(value: JsonScalar): JsonSchemaType {
	if (value === null) return 'null';
	if (typeof value === 'string') return 'string';
	if (typeof value === 'boolean') return 'boolean';
	return Number.isInteger(value) ? 'integer' : 'number';
}

function isJsonPrimitive(value: JsonValue): value is JsonScalar {
	return (
		value === null ||
		typeof value === 'string' ||
		typeof value === 'number' ||
		typeof value === 'boolean'
	);
}

function isJsonArray(value: JsonValue): value is JsonArray {
	return Array.isArray(value);
}

function isJsonObject(value: JsonValue): value is JsonObject {
	return value !== null && typeof value === 'object' && !Array.isArray(value);
}

function dedupeSchemas(schemas: JsonSchema[]): JsonSchema[] {
	const unique = new Map<string, JsonSchema>();
	for (const schema of schemas) {
		const key = JSON.stringify(schema);
		if (!unique.has(key)) {
			unique.set(key, schema);
		}
	}
	return Array.from(unique.values());
}

function cleanJsonValue(value: JsonValue, options: FormatOptions): JsonValue | undefined {
	if (value === null) {
		return options.removeNulls ? undefined : value;
	}

	if (Array.isArray(value)) {
		const nextArray = value
			.map((entry) => cleanJsonValue(entry, options))
			.filter((entry): entry is JsonValue => entry !== undefined);

		if (options.removeEmptyArrays && nextArray.length === 0) {
			return undefined;
		}

		return nextArray;
	}

	if (typeof value === 'object') {
		const nextObjectEntries = Object.entries(value)
			.map(([key, entry]) => [key, cleanJsonValue(entry, options)] as const)
			.filter(([, entry]) => entry !== undefined);

		const nextObject = Object.fromEntries(nextObjectEntries) as JsonObject;

		if (options.removeEmptyObjects && Object.keys(nextObject).length === 0) {
			return undefined;
		}

		return nextObject;
	}

	if (typeof value === 'string' && options.removeEmptyStrings && value.length === 0) {
		return undefined;
	}

	return value;
}

function sortJsonValue(value: JsonValue, direction: 'asc' | 'desc'): JsonValue {
	if (Array.isArray(value)) {
		return value.map((entry) => sortJsonValue(entry, direction));
	}

	if (value && typeof value === 'object') {
		const keys = Object.keys(value).sort((left, right) => {
			const comparison = left.localeCompare(right);
			return direction === 'asc' ? comparison : -comparison;
		});

		return Object.fromEntries(
			keys.map((key) => [key, sortJsonValue((value as JsonObject)[key], direction)])
		) as JsonObject;
	}

	return value;
}

function computeStats(value: JsonValue, original: string, formatted: string): JsonStats {
	const stats: JsonStats = {
		keys: 0,
		values: 0,
		strings: 0,
		numbers: 0,
		booleans: 0,
		nulls: 0,
		arrays: 0,
		objects: 0,
		maxDepth: 0,
		sizeOriginal: new TextEncoder().encode(original).length,
		sizeFormatted: new TextEncoder().encode(formatted).length
	};

	walkStats(value, 0, stats);
	return stats;
}

function walkStats(value: JsonValue, depth: number, stats: JsonStats): void {
	stats.maxDepth = Math.max(stats.maxDepth, depth);

	if (value === null) {
		stats.values++;
		stats.nulls++;
		return;
	}

	if (Array.isArray(value)) {
		stats.values++;
		stats.arrays++;
		for (const entry of value) {
			walkStats(entry, depth + 1, stats);
		}
		return;
	}

	if (typeof value === 'object') {
		stats.values++;
		stats.objects++;
		const entries = Object.entries(value);
		stats.keys += entries.length;
		for (const [, entry] of entries) {
			walkStats(entry, depth + 1, stats);
		}
		return;
	}

	stats.values++;
	switch (typeof value) {
		case 'string':
			stats.strings++;
			break;
		case 'number':
			stats.numbers++;
			break;
		case 'boolean':
			stats.booleans++;
			break;
	}
}

function renderJsonValue(
	value: JsonValue,
	depth: number,
	options: FormatOptions,
	forceInline: boolean
): string {
	if (value === null) {
		return 'null';
	}

	if (typeof value === 'string') {
		return quoteString(value, options.quoteStyle);
	}

	if (typeof value === 'number' || typeof value === 'boolean') {
		return String(value);
	}

	const indentUnit = options.indent === 'tab' ? '\t' : ' '.repeat(options.indent);
	const nextIndent = indentUnit.repeat(depth + 1);
	const currentIndent = indentUnit.repeat(depth);
	const shouldInline =
		forceInline ||
		options.collapseDepth === 0 ||
		(options.collapseDepth !== null && depth >= options.collapseDepth);

	if (Array.isArray(value)) {
		if (value.length === 0) {
			return '[]';
		}

		const items = value.map((entry) => renderJsonValue(entry, depth + 1, options, shouldInline));
		if (shouldInline) {
			return `[${items.join(', ')}${options.trailingCommas ? ',' : ''}]`;
		}

		return `[\n${items
			.map((entry, index) => `${nextIndent}${entry}${commaSuffix(index, items.length, options)}`)
			.join('\n')}\n${currentIndent}]`;
	}

	const entries = Object.entries(value);
	if (entries.length === 0) {
		return '{}';
	}

	const renderedEntries = entries.map(([key, entry]) => {
		const renderedKey = quoteString(key, options.quoteStyle);
		const renderedValue = renderJsonValue(entry, depth + 1, options, shouldInline);
		return `${renderedKey}: ${renderedValue}`;
	});

	if (shouldInline) {
		return `{ ${renderedEntries.join(', ')}${options.trailingCommas ? ',' : ''} }`;
	}

	return `{\n${renderedEntries
		.map(
			(entry, index) =>
				`${nextIndent}${entry}${commaSuffix(index, renderedEntries.length, options)}`
		)
		.join('\n')}\n${currentIndent}}`;
}

function commaSuffix(index: number, total: number, options: FormatOptions): string {
	if (index < total - 1) {
		return ',';
	}

	return options.trailingCommas ? ',' : '';
}

function quoteString(value: string, style: 'double' | 'single'): string {
	const escaped = value
		.replaceAll('\\', '\\\\')
		.replaceAll('\n', '\\n')
		.replaceAll('\r', '\\r')
		.replaceAll('\t', '\\t');

	if (style === 'single') {
		return `'${escaped.replaceAll("'", "\\'")}'`;
	}

	return `"${escaped.replaceAll('"', '\\"')}"`;
}

function inferGoDescriptor(samples: unknown[]): GoDescriptor {
	const definedSamples = samples.filter((sample) => sample !== undefined);
	if (definedSamples.length === 0) {
		return { kind: 'any' };
	}

	const nonNullSamples = definedSamples.filter((sample) => sample !== null);
	if (nonNullSamples.length === 0) {
		return { kind: 'any' };
	}

	if (nonNullSamples.every(isGoObjectSample)) {
		return inferGoObjectDescriptor(nonNullSamples as Record<string, unknown>[]);
	}

	if (nonNullSamples.every(Array.isArray)) {
		return inferGoArrayDescriptor(nonNullSamples as unknown[][]);
	}

	if (nonNullSamples.every((sample) => typeof sample === 'string')) {
		return { kind: 'primitive', type: 'string' };
	}

	if (nonNullSamples.every((sample) => typeof sample === 'boolean')) {
		return { kind: 'primitive', type: 'bool' };
	}

	if (nonNullSamples.every((sample) => typeof sample === 'number')) {
		return {
			kind: 'primitive',
			type: nonNullSamples.every((sample) => Number.isInteger(sample)) ? 'int64' : 'float64'
		};
	}

	return { kind: 'any' };
}

function inferGoObjectDescriptor(samples: Record<string, unknown>[]): GoDescriptor {
	const allKeys = new Set<string>();
	for (const sample of samples) {
		for (const key of Object.keys(sample)) {
			allKeys.add(key);
		}
	}

	const sortedKeys = Array.from(allKeys).sort((left, right) => left.localeCompare(right));
	const fields: GoFieldDescriptor[] = sortedKeys.map((jsonKey) => {
		let presentCount = 0;
		let nullable = false;
		const values: unknown[] = [];

		for (const sample of samples) {
			if (Object.prototype.hasOwnProperty.call(sample, jsonKey)) {
				presentCount += 1;
				const value = sample[jsonKey];
				if (value === null) {
					nullable = true;
				}
				values.push(value);
			}
		}

		const descriptor = inferGoDescriptor(values);
		return {
			jsonKey,
			descriptor,
			nullable,
			optional: presentCount < samples.length
		};
	});

	return {
		kind: 'object',
		fields
	};
}

function inferGoArrayDescriptor(samples: unknown[][]): GoDescriptor {
	const flattened = samples.flat();
	const nonNullElements = flattened.filter((item) => item !== null);
	return {
		kind: 'array',
		nullableElements: flattened.length > nonNullElements.length,
		element: inferGoDescriptor(nonNullElements)
	};
}

function resolveGoType(
	descriptor: GoDescriptor,
	context: GoGenerationContext,
	nameHint: string
): string {
	switch (descriptor.kind) {
		case 'primitive':
			return descriptor.type;
		case 'any':
			return 'any';
		case 'array': {
			const elementNameHint = nameHint.endsWith('Item') ? nameHint : `${nameHint}Item`;
			const elementType = resolveGoType(descriptor.element, context, elementNameHint);
			const listElementType =
				descriptor.nullableElements && canBePointerWrapped(elementType)
					? `*${elementType}`
					: elementType;
			return `[]${listElementType}`;
		}
		case 'object':
			return ensureStructFromDescriptor(descriptor, nameHint, context);
	}
}

function ensureStructFromDescriptor(
	descriptor: Extract<GoDescriptor, { kind: 'object' }>,
	nameHint: string,
	context: GoGenerationContext
): string {
	const signature = getDescriptorSignature(descriptor);
	const existing = context.nameBySignature.get(signature);
	if (existing) {
		return existing;
	}

	const typeName = reserveGoTypeName(nameHint, context);
	context.nameBySignature.set(signature, typeName);
	context.structOrder.push(typeName);

	const usedFieldNames = new Set<string>();
	const fieldLines = descriptor.fields.map((field) => {
		const baseName = toGoIdentifier(field.jsonKey, 'Field');
		const fieldName = reserveUniqueFieldName(baseName, usedFieldNames);
		let fieldType = resolveGoType(field.descriptor, context, `${typeName}${fieldName}`);
		const shouldUsePointer =
			context.options.usePointersForOptional &&
			(field.optional || field.nullable) &&
			canBePointerWrapped(fieldType);

		if (shouldUsePointer) {
			fieldType = `*${fieldType}`;
		}

		const structTag = buildGoStructTag(
			field.jsonKey,
			context.options.includeJsonTags,
			context.options.useOmitempty && (field.optional || field.nullable)
		);

		return `\t${fieldName} ${fieldType}${structTag}`;
	});

	const structDefinition =
		fieldLines.length > 0
			? `type ${typeName} struct {\n${fieldLines.join('\n')}\n}`
			: `type ${typeName} struct {}`;
	context.structDefinitionByName.set(typeName, structDefinition);

	return typeName;
}

function renderGoStructDefinitions(context: GoGenerationContext): string {
	return context.structOrder
		.map((typeName) => context.structDefinitionByName.get(typeName))
		.filter((definition): definition is string => Boolean(definition))
		.join('\n\n');
}

function getDescriptorSignature(descriptor: GoDescriptor): string {
	switch (descriptor.kind) {
		case 'primitive':
			return `primitive:${descriptor.type}`;
		case 'any':
			return 'any';
		case 'array':
			return `array:${descriptor.nullableElements ? 'nullable' : 'strict'}:${getDescriptorSignature(descriptor.element)}`;
		case 'object':
			return `object:${descriptor.fields
				.map((field) => {
					const optional = field.optional ? 'optional' : 'required';
					const nullable = field.nullable ? 'nullable' : 'strict';
					return `${field.jsonKey}:${optional}:${nullable}:${getDescriptorSignature(field.descriptor)}`;
				})
				.join('|')}`;
	}
}

function resolveRustType(
	descriptor: GoDescriptor,
	context: RustGenerationContext,
	nameHint: string
): string {
	switch (descriptor.kind) {
		case 'primitive':
			switch (descriptor.type) {
				case 'string':
					return 'String';
				case 'bool':
					return 'bool';
				case 'int64':
					return 'i64';
				case 'float64':
					return 'f64';
				default:
					return 'String';
			}
		case 'any':
			return 'serde_json::Value';
		case 'array': {
			const elementNameHint = nameHint.endsWith('Item') ? nameHint : `${nameHint}Item`;
			let elementType = resolveRustType(descriptor.element, context, elementNameHint);
			if (descriptor.nullableElements && canWrapRustOption(elementType)) {
				elementType = `Option<${elementType}>`;
			}
			return `Vec<${elementType}>`;
		}
		case 'object':
			return ensureRustStructFromDescriptor(descriptor, nameHint, context);
	}
}

function ensureRustStructFromDescriptor(
	descriptor: Extract<GoDescriptor, { kind: 'object' }>,
	nameHint: string,
	context: RustGenerationContext
): string {
	const signature = getDescriptorSignature(descriptor);
	const existing = context.nameBySignature.get(signature);
	if (existing) {
		return existing;
	}

	const typeName = reserveRustTypeName(nameHint, context);
	context.nameBySignature.set(signature, typeName);
	context.structOrder.push(typeName);

	const usedFieldNames = new Set<string>();
	const fieldLines = descriptor.fields.map((field) => {
		const baseSnake = toRustSnakeCase(field.jsonKey);
		const fieldName = reserveUniqueRustFieldName(
			sanitizeRustFieldIdentifier(baseSnake),
			usedFieldNames
		);
		const childHint = `${typeName}${toGoIdentifier(field.jsonKey, 'Field')}`;
		let rustType = resolveRustType(field.descriptor, context, childHint);
		const needsOption = (field.optional || field.nullable) && canWrapRustOption(rustType);
		if (needsOption) {
			rustType = `Option<${rustType}>`;
		}

		const serdeAttr = buildRustSerdeAttributes(field.jsonKey, needsOption);
		return `${serdeAttr}\n    pub ${fieldName}: ${rustType},`;
	});

	const body = fieldLines.length > 0 ? `\n${fieldLines.join('\n')}\n` : '';
	const structDefinition = `#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ${typeName} {${body}}`;
	context.structDefinitionByName.set(typeName, structDefinition);

	return typeName;
}

function renderRustStructDefinitions(context: RustGenerationContext): string {
	return context.structOrder
		.map((typeName) => context.structDefinitionByName.get(typeName))
		.filter((definition): definition is string => Boolean(definition))
		.join('\n\n');
}

function reserveRustTypeName(nameHint: string, context: RustGenerationContext): string {
	const normalized = toGoIdentifier(nameHint, 'AutoGenerated');
	let candidate = normalized;
	let suffix = 2;

	while (context.usedNames.has(candidate)) {
		candidate = `${normalized}${suffix}`;
		suffix += 1;
	}

	context.usedNames.add(candidate);
	return candidate;
}

function reserveUniqueRustFieldName(name: string, usedNames: Set<string>): string {
	let candidate = name;
	let suffix = 2;

	while (usedNames.has(candidate)) {
		candidate = `${name}_${suffix}`;
		suffix += 1;
	}

	usedNames.add(candidate);
	return candidate;
}

function toRustSnakeCase(jsonKey: string): string {
	const delimited = jsonKey.replaceAll(/([a-z0-9])([A-Z])/g, '$1_$2');
	const cleaned = delimited.replaceAll(/[^a-zA-Z0-9]+/g, '_').toLowerCase();
	const trimmed = cleaned.replace(/^_+|_+$/g, '');
	return trimmed.length > 0 ? trimmed : 'field';
}

function sanitizeRustFieldIdentifier(name: string): string {
	if (RUST_RESERVED_IDENTIFIERS.has(name)) {
		return `${name}_`;
	}
	return name;
}

function escapeRustSerdeRenameKey(key: string): string {
	return key.replaceAll('\\', '\\\\').replaceAll('"', '\\"');
}

function buildRustSerdeAttributes(jsonKey: string, useSkipSerializing: boolean): string {
	const escaped = escapeRustSerdeRenameKey(jsonKey);
	if (useSkipSerializing) {
		return `    #[serde(rename = "${escaped}", skip_serializing_if = "Option::is_none")]`;
	}
	return `    #[serde(rename = "${escaped}")]`;
}

function canWrapRustOption(rustType: string): boolean {
	return !rustType.startsWith('Option<');
}

