function toGoIdentifier(value: string, fallback: string): string {
	const normalized = value.replaceAll(/([a-z0-9])([A-Z])/g, '$1 $2');
	const tokens = normalized.match(/[A-Za-z0-9]+/g) ?? [];
	const identifier = tokens
		.map((token) => {
			if (/^\d+$/.test(token)) {
				return token;
			}

			const lower = token.toLowerCase();
			if (GO_COMMON_INITIALISMS.has(lower)) {
				return lower.toUpperCase();
			}

			return `${lower.charAt(0).toUpperCase()}${lower.slice(1)}`;
		})
		.join('');

	const fallbackIdentifier = `${fallback.charAt(0).toUpperCase()}${fallback.slice(1)}`;
	const safeIdentifier = identifier.length > 0 ? identifier : fallbackIdentifier;

	if (/^\d/.test(safeIdentifier)) {
		return `${fallbackIdentifier}${safeIdentifier}`;
	}

	return `${safeIdentifier.charAt(0).toUpperCase()}${safeIdentifier.slice(1)}`;
}

function normalizeGoPackageName(packageName: string): string {
	const normalized = packageName
		.toLowerCase()
		.replaceAll(/[^a-z0-9_]/g, '_')
		.replace(/^_+/g, '');

	if (normalized.length === 0 || /^\d/.test(normalized)) {
		return 'main';
	}

	return normalized;
}

function buildGoStructTag(key: string, includeTags: boolean, addOmitempty: boolean): string {
	if (!includeTags) {
		return '';
	}

	const suffix = addOmitempty ? ',omitempty' : '';
	const escapedKey = key.replaceAll('"', '\\"');
	return ` \`json:"${escapedKey}${suffix}"\``;
}

function canBePointerWrapped(goType: string): boolean {
	if (goType === 'any' || goType.startsWith('*')) {
		return false;
	}

	if (goType.startsWith('[]') || goType.startsWith('map[')) {
		return false;
	}

	return true;
}

function inferTypeScriptDescriptor(samples: unknown[]): TypeScriptDescriptor {
	const definedSamples = samples.filter((sample) => sample !== undefined);
	if (definedSamples.length === 0) {
		return { kind: 'primitive', type: 'unknown' };
	}

	const nonNullSamples = definedSamples.filter((sample) => sample !== null);
	const includesNull = nonNullSamples.length < definedSamples.length;

	if (nonNullSamples.length === 0) {
		return { kind: 'primitive', type: 'null' };
	}

	let descriptor: TypeScriptDescriptor;

	if (nonNullSamples.every(isTypeScriptObjectSample)) {
		descriptor = inferTypeScriptObjectDescriptor(nonNullSamples as Record<string, unknown>[]);
	} else if (nonNullSamples.every(Array.isArray)) {
		descriptor = inferTypeScriptArrayDescriptor(nonNullSamples as unknown[][]);
	} else if (nonNullSamples.every((sample) => typeof sample === 'string')) {
		descriptor = { kind: 'primitive', type: 'string' };
	} else if (nonNullSamples.every((sample) => typeof sample === 'number')) {
		descriptor = { kind: 'primitive', type: 'number' };
	} else if (nonNullSamples.every((sample) => typeof sample === 'boolean')) {
		descriptor = { kind: 'primitive', type: 'boolean' };
	} else {
		descriptor = createTypeScriptUnionDescriptor(
			nonNullSamples.map((sample) => inferTypeScriptDescriptor([sample]))
		);
	}

	if (!includesNull) {
		return descriptor;
	}

	return createTypeScriptUnionDescriptor([descriptor, { kind: 'primitive', type: 'null' }]);
}

function inferTypeScriptObjectDescriptor(samples: Record<string, unknown>[]): TypeScriptDescriptor {
	const allKeys = new Set<string>();
	for (const sample of samples) {
		for (const key of Object.keys(sample)) {
			allKeys.add(key);
		}
	}

	const sortedKeys = Array.from(allKeys).sort((left, right) => left.localeCompare(right));
	const properties: TypeScriptPropertyDescriptor[] = sortedKeys.map((jsonKey) => {
		let presentCount = 0;
		const values: unknown[] = [];

		for (const sample of samples) {
			if (!Object.prototype.hasOwnProperty.call(sample, jsonKey)) {
				continue;
			}

			presentCount += 1;
			values.push(sample[jsonKey]);
		}

		return {
			jsonKey,
			descriptor: inferTypeScriptDescriptor(values),
			optional: presentCount < samples.length
		};
	});

	return {
		kind: 'object',
		properties
	};
}

function inferTypeScriptArrayDescriptor(samples: unknown[][]): TypeScriptDescriptor {
	const flattened = samples.flat();
	if (flattened.length === 0) {
		return {
			kind: 'array',
			element: { kind: 'primitive', type: 'unknown' }
		};
	}

	return {
		kind: 'array',
		element: inferTypeScriptDescriptor(flattened)
	};
}

function createTypeScriptUnionDescriptor(variants: TypeScriptDescriptor[]): TypeScriptDescriptor {
	const flattened = flattenTypeScriptUnionVariants(variants);
	const uniqueBySignature = new Map<string, TypeScriptDescriptor>();

	for (const variant of flattened) {
		const signature = getTypeScriptDescriptorSignature(variant);
		if (!uniqueBySignature.has(signature)) {
			uniqueBySignature.set(signature, variant);
		}
	}

	const normalized = Array.from(uniqueBySignature.values()).sort(compareTypeScriptDescriptors);
	if (normalized.length === 1) {
		return normalized[0];
	}

	return {
		kind: 'union',
		variants: normalized
	};
}

function flattenTypeScriptUnionVariants(variants: TypeScriptDescriptor[]): TypeScriptDescriptor[] {
	const flattened: TypeScriptDescriptor[] = [];

	for (const variant of variants) {
		if (variant.kind === 'union') {
			flattened.push(...flattenTypeScriptUnionVariants(variant.variants));
			continue;
		}

		flattened.push(variant);
	}

	return flattened;
}

function compareTypeScriptDescriptors(
	left: TypeScriptDescriptor,
	right: TypeScriptDescriptor
): number {
	const rankDiff = getTypeScriptDescriptorRank(left) - getTypeScriptDescriptorRank(right);
	if (rankDiff !== 0) {
		return rankDiff;
	}

	return getTypeScriptDescriptorSignature(left).localeCompare(
		getTypeScriptDescriptorSignature(right)
	);
}

function getTypeScriptDescriptorRank(descriptor: TypeScriptDescriptor): number {
	if (descriptor.kind === 'primitive') {
		switch (descriptor.type) {
			case 'string':
				return 0;
			case 'number':
				return 1;
			case 'boolean':
				return 2;
			case 'unknown':
				return 6;
			case 'null':
				return 7;
		}
	}

	if (descriptor.kind === 'object') {
		return 3;
	}

	if (descriptor.kind === 'array') {
		return 4;
	}

	return 5;
}

function resolveTypeScriptType(
	descriptor: TypeScriptDescriptor,
	context: TypeScriptGenerationContext,
	nameHint: string
): string {
	switch (descriptor.kind) {
		case 'primitive':
			return descriptor.type;
		case 'array': {
			const elementType = resolveTypeScriptType(descriptor.element, context, `${nameHint}Item`);
			const normalized = needsParenthesizedArrayElementType(elementType)
				? `(${elementType})`
				: elementType;
			return `${normalized}[]`;
		}
		case 'object':
			return ensureTypeScriptInterface(descriptor, nameHint, context);
		case 'union':
			return descriptor.variants
				.map((variant, index) =>
					resolveTypeScriptType(variant, context, `${nameHint}Variant${index + 1}`)
				)
				.join(' | ');
	}
}

function ensureTypeScriptInterface(
	descriptor: Extract<TypeScriptDescriptor, { kind: 'object' }>,
	nameHint: string,
	context: TypeScriptGenerationContext
): string {
	const signature = getTypeScriptDescriptorSignature(descriptor);
	const existing = context.interfaceNameBySignature.get(signature);
	if (existing) {
		return existing;
	}

	const typeName = reserveTypeScriptTypeName(nameHint, context);
	context.interfaceNameBySignature.set(signature, typeName);
	context.interfaceOrder.push(typeName);

	const exportKeyword = context.options.exportNamedTypes ? 'export ' : '';
	const suffix = context.options.useSemicolons ? ';' : '';
	const propertyLines = descriptor.properties.map((property) => {
		const propertyName = toTypeScriptPropertyName(property.jsonKey);
		const propertyType = resolveTypeScriptType(
			property.descriptor,
			context,
			`${typeName}${toTypeScriptTypeName(property.jsonKey, 'Field')}`
		);
		const optionalMarker = property.optional ? '?' : '';
		return `\t${propertyName}${optionalMarker}: ${propertyType}${suffix}`;
	});

	const definition =
		propertyLines.length > 0
			? `${exportKeyword}interface ${typeName} {\n${propertyLines.join('\n')}\n}`
			: `${exportKeyword}interface ${typeName} {}`;

	context.interfaceDefinitionByName.set(typeName, definition);
	return typeName;
}

function renderTypeScriptInterfaces(context: TypeScriptGenerationContext): string {
	return context.interfaceOrder
		.map((typeName) => context.interfaceDefinitionByName.get(typeName))
		.filter((definition): definition is string => Boolean(definition))
		.join('\n\n');
}

function reserveTypeScriptTypeName(nameHint: string, context: TypeScriptGenerationContext): string {
	const normalized = toTypeScriptTypeName(nameHint, 'AutoGenerated');
	let candidate = normalized;
	let suffix = 2;

	while (context.usedNames.has(candidate)) {
		candidate = `${normalized}${suffix}`;
		suffix += 1;
	}

	context.usedNames.add(candidate);
	return candidate;
}

function toTypeScriptTypeName(value: string, fallback: string): string {
	const normalized = value.replaceAll(/([a-z0-9])([A-Z])/g, '$1 $2');
	const tokens = normalized.match(/[A-Za-z0-9]+/g) ?? [];
	const identifier = tokens
		.map((token) => {
			if (/^\d+$/.test(token)) {
				return token;
			}

			const lower = token.toLowerCase();
			return `${lower.charAt(0).toUpperCase()}${lower.slice(1)}`;
		})
		.join('');

	const fallbackIdentifier = `${fallback.charAt(0).toUpperCase()}${fallback.slice(1)}`;
	let safeIdentifier = identifier.length > 0 ? identifier : fallbackIdentifier;

	if (/^\d/.test(safeIdentifier)) {
		safeIdentifier = `${fallbackIdentifier}${safeIdentifier}`;
	}

	if (TYPESCRIPT_RESERVED_IDENTIFIERS.has(safeIdentifier.toLowerCase())) {
		safeIdentifier = `${safeIdentifier}Type`;
	}

	return `${safeIdentifier.charAt(0).toUpperCase()}${safeIdentifier.slice(1)}`;
}

function toTypeScriptPropertyName(jsonKey: string): string {
	if (/^[$A-Za-z_][$A-Za-z0-9_]*$/.test(jsonKey)) {
		return jsonKey;
	}

	const escaped = jsonKey.replaceAll('\\', '\\\\').replaceAll('"', '\\"');
	return `"${escaped}"`;
}

function needsParenthesizedArrayElementType(elementType: string): boolean {
	return elementType.includes(' | ') || elementType.includes('&');
}

function getTypeScriptDescriptorSignature(descriptor: TypeScriptDescriptor): string {
	switch (descriptor.kind) {
		case 'primitive':
			return `primitive:${descriptor.type}`;
		case 'array':
			return `array:${getTypeScriptDescriptorSignature(descriptor.element)}`;
		case 'object':
			return `object:${descriptor.properties
				.map(
					(property) =>
						`${property.jsonKey}:${property.optional ? 'optional' : 'required'}:${getTypeScriptDescriptorSignature(property.descriptor)}`
				)
				.join('|')}`;
		case 'union': {
			const variantSignatures = descriptor.variants
				.map((variant) => getTypeScriptDescriptorSignature(variant))
				.sort((left, right) => left.localeCompare(right));
			return `union:${variantSignatures.join('|')}`;
		}
	}
}

function isTypeScriptObjectSample(sample: unknown): sample is Record<string, unknown> {
	return sample !== null && typeof sample === 'object' && !Array.isArray(sample);
}

function isGoObjectSample(sample: unknown): sample is Record<string, unknown> {
	return sample !== null && typeof sample === 'object' && !Array.isArray(sample);
}
