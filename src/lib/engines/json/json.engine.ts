export async function toMarkdownTable(json: string): Promise<string> {
	const parsed = JSON.parse(json) as JsonValue;
	if (!Array.isArray(parsed) || parsed.length === 0) {
		throw new Error('Input must be a non-empty array of objects for Markdown Table conversion');
	}

	const keys = Array.from(
		new Set(
			parsed.flatMap((row) =>
				row && typeof row === 'object' && !Array.isArray(row) ? Object.keys(row) : []
			)
		)
	);

	let markdown = `| ${keys.join(' | ')} |\n`;
	markdown += `| ${keys.map(() => '---').join(' | ')} |\n`;

	for (const row of parsed) {
		if (!row || typeof row !== 'object' || Array.isArray(row)) {
			throw new Error('Each array item must be an object for Markdown Table conversion');
		}

		markdown += `| ${keys
			.map((key) => formatMarkdownCell((row as JsonObject)[key]))
			.join(' | ')} |\n`;
	}

	return markdown;
}

export function toGoStructs(json: string, options: Partial<GoStructOptions> = {}): string {
	const parsed = parseJSON(json);
	if (!parsed.success) {
		throw new Error(parsed.error.plainLanguageExplanation);
	}

	const resolved: GoStructOptions = {
		...DEFAULT_GO_STRUCT_OPTIONS,
		...options,
		rootName: options.rootName?.trim() || DEFAULT_GO_STRUCT_OPTIONS.rootName,
		packageName: options.packageName?.trim() || ''
	};

	const rootDescriptor = inferGoDescriptor([parsed.data]);
	const context: GoGenerationContext = {
		options: resolved,
		usedNames: new Set<string>(),
		nameBySignature: new Map<string, string>(),
		structDefinitionByName: new Map<string, string>(),
		structOrder: []
	};

	const outputSections: string[] = [];

	if (resolved.packageName) {
		outputSections.push(`package ${normalizeGoPackageName(resolved.packageName)}`);
	}

	if (rootDescriptor.kind === 'object') {
		ensureStructFromDescriptor(rootDescriptor, resolved.rootName, context);
		outputSections.push(renderGoStructDefinitions(context));
		return outputSections.filter((section) => section.length > 0).join('\n\n');
	}

	const rootTypeName = reserveGoTypeName(resolved.rootName, context);
	const rootType = resolveGoType(rootDescriptor, context, `${rootTypeName}Item`);
	outputSections.push(`type ${rootTypeName} ${rootType}`);

	if (context.structOrder.length > 0) {
		outputSections.push(renderGoStructDefinitions(context));
	}

	return outputSections.filter((section) => section.length > 0).join('\n\n');
}

export function toTypeScriptTypes(
	json: string,
	options: Partial<TypeScriptTypeOptions> = {}
): string {
	const parsed = parseJSON(json);
	if (!parsed.success) {
		throw new Error(parsed.error.plainLanguageExplanation);
	}

	const resolved: TypeScriptTypeOptions = {
		...DEFAULT_TYPESCRIPT_TYPE_OPTIONS,
		...options,
		rootName: options.rootName?.trim() || DEFAULT_TYPESCRIPT_TYPE_OPTIONS.rootName
	};

	const rootDescriptor = inferTypeScriptDescriptor([parsed.data]);
	const context: TypeScriptGenerationContext = {
		options: resolved,
		usedNames: new Set<string>(),
		interfaceNameBySignature: new Map<string, string>(),
		interfaceDefinitionByName: new Map<string, string>(),
		interfaceOrder: []
	};

	const outputSections: string[] = [];
	let rootTypeName = '';
	let rootType: string | null = null;

	if (rootDescriptor.kind === 'object') {
		rootTypeName = ensureTypeScriptInterface(rootDescriptor, resolved.rootName, context);
	} else {
		rootTypeName = reserveTypeScriptTypeName(resolved.rootName, context);
		rootType = resolveTypeScriptType(rootDescriptor, context, rootTypeName);
	}

	const interfaceDefinitions = renderTypeScriptInterfaces(context);

	if (rootType) {
		const exportKeyword = resolved.exportNamedTypes ? 'export ' : '';
		const suffix = resolved.useSemicolons ? ';' : '';
		outputSections.push(`${exportKeyword}type ${rootTypeName} = ${rootType}${suffix}`);
	}

	if (interfaceDefinitions.length > 0) {
		outputSections.push(interfaceDefinitions);
	}

	return outputSections.join('\n\n');
}

